<h2>Directory structure:</h2>
<ul>
  <br />
  <li> /application - the MW system directory where the system files are located.    </li>
  <li>/application/functions - the main functions of MW    </li>
  <li>/userfiles/modules - the directory where the modules are installed    </li>
  <li>/userfiles/templates - the directory for the templates </li>
</ul>
<h2>Gettings data:  </h2>
<p>All the data you get with those functions is returned as array</p>
<p>The get() function is the core function that you shall use to query the dastabase. </p>
<p>You can use it by passing parameters via array or string. The &quot;from&quot; parameter is the name of the dabase table you want to query, and the other parameters are used to filter and sort the results. </p>
<p>way get(&quot;from=content&amp;id=1&quot;); </p>

  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
  
